def save_history(history, new_articles):
    cutoff = datetime.datetime.now() - datetime.timedelta(days=DEDUP_DAYS)
    history["articles"] = [
        a for a in history["articles"]
        if datetime.datetime.fromisoformat(a["date"]) > cutoff
    ]
    for article in new_articles:
        title_key = re.sub(r"\s+", "", article.get("zh_title", article["title"]).lower())
        history["articles"].append({
            "url_hash": hashlib.md5(article["url"].encode()).hexdigest(),
            "title_hash": hashlib.md5(title_key.encode()).hexdigest(),
            "title": article.get("zh_title", article["title"]),
            "url": article["url"],
            "date": datetime.datetime.now().isoformat(),
        })
    with open(HISTORY_FILE, "w") as f:
        json.dump(history, f, ensure_ascii=False, indent=2)


def _url_hash(url):
    return hashlib.md5(url.encode()).hexdigest()


def _title_hash(title):
    return hashlib.md5(re.sub(r"\s+", "", title.lower()).encode()).hexdigest()


def is_duplicate(article, history):
    uh = _url_hash(article["url"])
    th = _title_hash(article["title"])
    for h in history["articles"]:
        if h["url_hash"] == uh or h["title_hash"] == th:
            return True
    return False


def dedup_batch(articles):
    seen_u, seen_t, unique = set(), set(), []
    for a in articles:
        uh, th = _url_hash(a["url"]), _title_hash(a["title"])
        if uh in seen_u or th in seen_t:
            continue
        seen_u.add(uh)
        seen_t.add(th)
        unique.append(a)
    return unique


def strip_html(text):
    return re.sub(r"<[^>]+>", "", text or "").strip()


_TARGET_DATE: datetime.datetime | None = None  # set by --date flag


def _today_start():
    if _TARGET_DATE:
        return _TARGET_DATE + datetime.timedelta(days=1)  # "today" = day after target
    now = datetime.datetime.now()
    return datetime.datetime(now.year, now.month, now.day)


def _tomorrow_start():
    return _today_start() + datetime.timedelta(days=1)


def _yesterday_start():
    if _TARGET_DATE:
        return _TARGET_DATE  # "yesterday" = the target date itself
    return _today_start() - datetime.timedelta(days=1)


def _parse_date_text(text):
    text = (text or "").strip()
    if not text:
        return None

    now = datetime.datetime.now()
    if "刚刚" in text:
        return now

    m = re.search(r"(\d+)\s*分钟前", text)
    if m:
        return now - datetime.timedelta(minutes=int(m.group(1)))

    m = re.search(r"(\d+)\s*小时前", text)
    if m:
        return now - datetime.timedelta(hours=int(m.group(1)))

    m = re.search(r"(\d+)\s*天前", text)
    if m:
        return now - datetime.timedelta(days=int(m.group(1)))

    for pattern in (
        r"(20\d{2})[-/.年](\d{1,2})[-/.月](\d{1,2})",
        r"(\d{1,2})[-/.月](\d{1,2})",
    ):
        m = re.search(pattern, text)
        if not m:
            continue
        try:
            if len(m.groups()) == 3:
                year, month, day = map(int, m.groups())
            else:
                year = now.year
                month, day = map(int, m.groups())
            return datetime.datetime(year, month, day)
        except ValueError:
            return None

    return None


def _parse_published_date(text):
    text = (text or "").strip()
    if not text:
        return None

    for candidate in (text, text.replace("Z", "+00:00")):
        try:
            parsed = datetime.datetime.fromisoformat(candidate)
            if parsed.tzinfo:
                parsed = parsed.astimezone().replace(tzinfo=None)
            return parsed
        except ValueError:
            pass

    return _parse_date_text(text)


def _contains_old_year(article):
    current_year = datetime.datetime.now().year
    # Only check URL — titles/summaries legitimately reference past years in analysis
    url = article.get("url", "")
    for year_s in re.findall(r"(?<!\d)(20\d{2})(?!\d)", url):
        if int(year_s) < current_year:
            return True
    return False


def is_fresh(article, require_date=False):
    if _contains_old_year(article):
        return False

    published = article.get("published_at")
    if not published:
        return not require_date

    parsed = _parse_published_date(published)
    if not parsed:
        return not require_date

    return _yesterday_start() <= parsed < _today_start()


def filter_fresh(articles, require_date=False, label="Freshness"):
    kept = [a for a in articles if is_fresh(a, require_date=require_date)]
    removed = len(articles) - len(kept)
    if removed:
        log(f"{label} removed: {removed}")
    return kept


def sort_candidate_articles(articles):
    def parsed_time(article):
        parsed = _parse_published_date(article.get("published_at", ""))
        return parsed or datetime.datetime.min

    return sorted(
        articles,
        key=lambda a: (
            1 if a.get("priority") else 0,
            1 if str(a.get("source", "")).startswith("微信-") else 0,
            parsed_time(a),
        ),
        reverse=True,
    )


