def _parse_json_robust(raw):
    """Try multiple strategies to parse a JSON array from Claude's response."""
    # Strip code fences
    raw = re.sub(r"^```\w*\n?|```\s*$", "", raw.strip()).strip()

    # Strategy 1: direct parse
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        pass

    # Strategy 2: find the outermost [...] and parse
    m = re.search(r"\[.*\]", raw, re.DOTALL)
    if m:
        try:
            return json.loads(m.group())
        except json.JSONDecodeError:
            pass

    # Strategy 3: extract individual {...} objects via regex and parse each
    objects = re.findall(r'\{[^{}]*\}', raw, re.DOTALL)
    results = []
    for obj in objects:
        try:
            results.append(json.loads(obj))
        except json.JSONDecodeError:
            pass
    if results:
        return results

    return None


def format_message(articles, date_str, on_this_day=None):
    # Group by explicit category assigned by Claude
    deep    = [a for a in articles if a.get("category") == "深度拆解"]
    news    = [a for a in articles if a.get("category") == "行业资讯"]
    general = [a for a in articles if a.get("category") == "泛游戏"]

    # Feishu plain-text format — no markdown (** [] _ --- are rendered literally)
    lines = [
        f"🎮 休闲游戏分析日报 · {date_str}  共 {len(articles)} 篇",
    ]

    sections = [
        ("🔥 深度拆解休闲游戏玩法", deep),
        ("📊 游戏行业资讯",          news),
        ("🕹️ 泛游戏内容",            general),
    ]

    idx = 1
    for section_title, group in sections:
        if not group:
            continue
        lines.append(f"\n━━ {section_title}\n")
        for a in group:
            title = a['zh_title'].replace('《', '').replace('》', '')
            lines.append(f"{idx}. {title}")
            if a.get("zh_desc"):
                lines.append(a["zh_desc"])
            lines.append(f"来源：{a['source']}")
            lines.append(a["url"] + "\n")
            idx += 1

    # On This Day section
    if on_this_day:
        # Group by year
        by_year = {}
        for a in on_this_day:
            y = a["on_this_day_year"]
            by_year.setdefault(y, []).append(a)

        lines.append("\n━━ 📅 往年今日（{month_day}）\n".format(
            month_day=date_str[5:]))
        for year in sorted(by_year.keys(), reverse=True):
            lines.append(f"▎{year}年")
            for a in by_year[year]:
                lines.append(f"· {a['title'].replace('《', '').replace('》', '')}")
                if a.get("zh_desc"):
                    lines.append(f"  {a['zh_desc']}")
                lines.append(f"  来源：{a['source']}")
                lines.append(f"  {a['url']}")
            lines.append("")

    lines.append("由 Claude 自动整理，每日 09:00 触发发送")
    return "\n".join(lines)


def send_feishu(message, test_mode=False):
    if test_mode:
        # Test: send to personal chat only
        cmd = ["lark-cli", "im", "+messages-send",
               "--user-id", FEISHU_USER_ID,
               "--text", message, "--as", "user"]
        target = f"user:{FEISHU_USER_ID}"
    else:
        # Production: send to group chat
        cmd = ["lark-cli", "im", "+messages-send",
               "--chat-id", FEISHU_CHAT_ID,
               "--text", message, "--as", "user"]
        target = f"chat:{FEISHU_CHAT_ID}"

    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode != 0:
        log(f"Feishu error: {result.stderr}")
        return False
    log(f"Feishu: sent successfully → {target}")
    return True


def main(test_mode=False):
    log("=== Game Digest Start ===" + (" [TEST MODE]" if test_mode else ""))

    # 防重复发送：当天已有存档文件则跳过（test_mode 和手动指定日期除外）
    if not test_mode and not _TARGET_DATE:
        today_archive = ARCHIVE_DIR / f"{datetime.datetime.now().strftime('%Y-%m-%d')}.md"
        if today_archive.exists():
            log(f"今日日报已发送（{today_archive.name}），跳过重复运行")
            return

    # Collect
    on_this_day = fetch_on_this_day()
    all_articles = fetch_wechat_rss() + fetch_rss() + fetch_podcasts() + fetch_search() + fetch_focus_app_search()
    if ENABLE_SOGOU_WECHAT:
        all_articles += fetch_wechat()
    else:
        log("Search (WeChat/Sogou): disabled")

    # Remove blocked domains (official game sites, app stores, social media)
    all_articles = filter_blocked(all_articles)

    # Remove articles whose URL contains an old year (e.g. /2025/ or /2024/)
    before = len(all_articles)
    all_articles = [a for a in all_articles if not _contains_old_year(a)]
    removed = before - len(all_articles)
    if removed:
        log(f"Old URL year removed: {removed}")

    # Dedup within batch (same article from multiple sources)
    all_articles = dedup_batch(all_articles)
    log(f"After batch dedup: {len(all_articles)}")

    # Dedup against history (skip in test mode)
    if test_mode:
        log("History dedup: skipped (test mode)")
        candidates_pool = all_articles
    else:
        history = load_history()
        log(f"History: {len(history['articles'])} entries (last {DEDUP_DAYS} days)")
        candidates_pool = [a for a in all_articles if not is_duplicate(a, history)]
        log(f"After history dedup: {len(candidates_pool)}")

    if not candidates_pool:
        log("No articles today. Skipping send.")
        return

    candidates = sort_candidate_articles(candidates_pool)[:MAX_CANDIDATES_TO_SCORE]
    log(f"Candidates selected for scoring: {len(candidates)}")

    # Score + translate in batches of 20 (balance: fewer CLI cold-starts vs timeout risk)
    scored = []
    BATCH = 20
    for i in range(0, len(candidates), BATCH):
        batch_result = score_and_translate_batch(candidates[i:i + BATCH])
        scored.extend(batch_result)
    log(f"After scoring (≥{MIN_SCORE}): {len(scored)} articles")

    # Generate descriptions for On This Day articles (title-only, bypass score filter)
    if on_this_day:
        on_this_day = translate_on_this_day(on_this_day)

    if not scored:
        log("No articles scored above threshold today.")
        return

    # Sort by score desc within each category (format_message handles grouping)
    scored.sort(key=lambda x: x["score"], reverse=True)
    log(f"Sending {len(scored)} articles")

    # Format and send — use target date if backfilling, otherwise today
    report_date = _TARGET_DATE if _TARGET_DATE else datetime.datetime.now()
    date_str = report_date.strftime("%Y年%m月%d日")
    message = format_message(scored, date_str, on_this_day=on_this_day or None)
    send_feishu(message, test_mode=test_mode)

    # Save local backup
    ARCHIVE_DIR.mkdir(exist_ok=True)
    suffix = "_test" if test_mode else ""
    archive_file = ARCHIVE_DIR / f"{report_date.strftime('%Y-%m-%d')}{suffix}.md"
    with open(archive_file, "w", encoding="utf-8") as f:
        f.write(message)
    log(f"Saved: {archive_file.name}")

    # Save to history (skip in test mode to avoid polluting dedup)
    if not test_mode:
        save_history(history, scored)

    log(f"=== Done: {len(scored)} articles sent ===")


