def _gnews_search(query, lang, hl, gl, ceid, n=10):
    """Search via Google News RSS. Returns articles with reliable pubDate."""
    url = (
        f"https://news.google.com/rss/search"
        f"?q={requests.utils.quote(query)}"
        f"&hl={hl}&gl={gl}&ceid={ceid}"
    )
    cutoff = _today_start() - datetime.timedelta(days=7)
    try:
        feed = feedparser.parse(url)
    except Exception:
        return []
    articles = []
    for entry in feed.entries[:n]:
        pub = None
        for attr in ("published_parsed", "updated_parsed"):
            val = getattr(entry, attr, None)
            if val:
                try:
                    pub = datetime.datetime(*val[:6])
                except Exception:
                    pass
                break
        if not pub or pub < cutoff:
            continue
        link = getattr(entry, "link", "")
        if not link:
            continue
        articles.append({
            "title": strip_html(getattr(entry, "title", "")),
            "url": link,
            "summary": strip_html(getattr(entry, "summary", ""))[:600],
            "source": "Google News",
            "lang": lang,
            "published_at": pub.isoformat(),
        })
    return articles


